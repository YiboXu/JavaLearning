package tk.spring.annotion.model;

import org.springframework.beans.factory.annotation.Value;

public class Member {
    @Value("${member.name}")
    private String name;
    @Value("${member.age}")
    private int age;



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Member{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
